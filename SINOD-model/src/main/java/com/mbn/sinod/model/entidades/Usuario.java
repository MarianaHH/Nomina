package com.mbn.sinod.model.entidades;

public class Usuario {

    
    private int cod_tipousuario;
    private int cod_usuario;
    private int cod_sistema;
    private int cod_rol;

    /**
     * @return the cod_tipousuario
     */
    public int getCod_tipousuario() {
        return cod_tipousuario;
    }

    /**
     * @param cod_tipousuario the cod_tipousuario to set
     */
    public void setCod_tipousuario(int cod_tipousuario) {
        this.cod_tipousuario = cod_tipousuario;
    }

    /**
     * @return the cod_usuario
     */
    public int getCod_usuario() {
        return cod_usuario;
    }

    /**
     * @param cod_usuario the cod_usuario to set
     */
    public void setCod_usuario(int cod_usuario) {
        this.cod_usuario = cod_usuario;
    }

    /**
     * @return the cod_sistema
     */
    public int getCod_sistema() {
        return cod_sistema;
    }

    /**
     * @param cod_sistema the cod_sistema to set
     */
    public void setCod_sistema(int cod_sistema) {
        this.cod_sistema = cod_sistema;
    }

    /**
     * @return the cod_rol
     */
    public int getCod_rol() {
        return cod_rol;
    }

    /**
     * @param cod_rol the cod_rol to set
     */
    public void setCod_rol(int cod_rol) {
        this.cod_rol = cod_rol;
    }

}
